#30%, 60%, 90% de un numero

num = float(input("digite su numero: "))

porcentaje_1 = 30
porcentaje_2 = 60
porcentaje_3 = 90

calculo_p1 = num * porcentaje_1 / 100
calculo_p2 = num * porcentaje_2 / 100
calculo_p3 = num * porcentaje_3 / 100

print (f"el 30 porciento del numero es: {calculo_p1}")
print (f"el 60 porciento del numero es: {calculo_p2}")
print (f"el 90 porciento del numero es: {calculo_p3}")